basic_calc = gets.chomp #crucial, no idea why. WHY DOES THIS NEED TO BE HERE?
case basic_calc
when "a" # adding
	puts "Enter two numbers "
	x = gets.chomp.to_i
	y = gets.chomp.to_i
	puts "=" + (x + y).to_s
	mainmenu

when "s" # subtracting
	puts "Enter two numbers "
	x = gets.chomp.to_i
	y = gets.chomp.to_i
	puts "=" + (x - y).to_s
	mainmenu

when "m" #multiplying
	puts "Enter two numbers "
	x = gets.chomp.to_i
	y = gets.chomp.to_i
	puts "=" + (x * y).to_s
	mainmenu

when "d" #dividing
	puts "Enter two numbers "
	x = gets.chomp.to_f
	y = gets.chomp.to_f
	puts "=" + (x / y).to_s
	mainmenu

when "p" #power	
	puts "Enter two numbers "
	x = gets.chomp.to_f
	y = gets.chomp.to_f
	puts "=" + (x ** y).to_s
	mainmenu

when "r" #sqaure root
	puts "Enter number "
	x = gets.chomp.to_f	
	puts "=" + Math.sqrt(x).to_s
	mainmenu

when "g" #mortgage
	puts "Enter principle of loan "
	p = gets.chomp.to_f
	puts "Enter annual interest rate "
	i = gets.chomp.to_f / 100 / 12
	puts "Enter number of payments"
	n = gets.chomp.to_f
	puts "=" + m = ( ( (i * ( ( 1 + i ) ** n ) ) / ( ( ( 1 + i ) ** n ) - 1 )) * p ).to_s
	mainmenu

when "mi" #bmi 
	puts "enter height"
	ht = gets.chomp.to_f
	puts "enter weight"
	wt = gets.chomp.to_f
	bmi = ( (wt) / ( (ht) * (ht) ) ).to_s #calculating the bmi
	puts "your bmi is #{bmi}"
	mainmenu

when 't' #trip
	puts "enter distance"
	d = gets.to_f
	puts "how many miles to the gallon? "
	mpg = gets.to_f
	puts "how much does fuel cost per gallon?"
	$pg = gets.to_f
	puts "enter speed"
	speed = gets.to_f

	if speed > 60
		for speed in (61..speed) # loop code "For every 1 MPH over 60 MPH, reduce the the MPG by 2 MPG."
			mpg -= 2		
		end
	end

	time = (d / speed)
	cost = (mpg * $pg)
	puts "Your trip will take #{ time } hours and cost $#{ cost }."
	mainmenu
end

                                                                                         
                                                                                                                                  
                                                                                                                                  
